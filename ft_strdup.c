#include "libft.h"

char		*ft_strdup(const char *s1)
{
	size_t		i;
	char		*str;

	i = 0;
	if (!(str = (char *)malloc(1 + ft_strlen(s1) * sizeof(char))))
		return (0);
	while (s1[i])
	{
		str[i] = s1[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
